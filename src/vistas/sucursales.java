    private class GrillaSucursales extends Thread {

        public void run() {

            //Antes de Cargar el Jtable Ajustamos el ancho de las columnas con el Ancho que se nos antoje
            int cantidadRegistro = modelo.getRowCount();
            for (int i = 1; i <= cantidadRegistro; i++) {
                modelo.removeRow(0);
            }

            sucursalDAO DAO = new sucursalDAO();
            try {
                for (sucursal usu : DAO.todos()) {
                    String Datos[] = {String.valueOf(usu.getCodigo()),
                        usu.getNombre()};
                    modelo.addRow(Datos);
                }
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "ERROR: " + e.getMessage());
            }
            tablasucursales.setRowSorter(new TableRowSorter(modelo));
            int cantFilas = tablasucursales.getRowCount();
            if (cantFilas > 0) {
                BotonEditar.setEnabled(true);
                BotonBorrar.setEnabled(true);
                BotonImprimir.setEnabled(true);
            } else {
                BotonEditar.setEnabled(false);
                BotonBorrar.setEnabled(false);
                BotonImprimir.setEnabled(false);
            }
            tablasucursales.requestFocus();
        }
    }


