    private void BotonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonEditarActionPerformed
        this.limpiar();
        int nFila = this.tablasucursales.getSelectedRow();
        this.codigo.setText(this.tablasucursales.getValueAt(nFila, 0).toString());
        sucursalDAO usu = new sucursalDAO();
        sucursal u = null;
        try {
            u = usu.buscarId(Integer.valueOf(codigo.getText()));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR:" + e.getMessage());
        }
        if (u != null) {
            nombre.setText(u.getNombre());
            nombrefacturasuc.setText(u.getNombrefacturasuc());
            nombreremisionsuc.setText(u.getNombreremisionsuc());
            impresorafacturasuc.setText(u.getImpresorafacturasuc());
            impresoraremisionsuc.setText(u.getImpresoraremisionsuc());
        }

        codigo.setEnabled(true);
        nombre.requestFocus();
        this.AbrirVentana();
        // TODO add your handling code here:
    }//GEN-LAST:event_BotonEditarActionPerformed

    private void BotonGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonGrabarActionPerformed
        if (this.nombre.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese el Nombre de la Sucursal");
            this.nombre.requestFocus();
            return;
        }

        sucursalDAO grab = new sucursalDAO();
        sucursal us = new sucursal();

        us.setNombre(nombre.getText());
        us.setNombrefacturasuc(nombrefacturasuc.getText());
        us.setNombreremisionsuc(nombreremisionsuc.getText());
        us.setImpresorafacturasuc(impresorafacturasuc.getText());
        us.setImpresoraremisionsuc(impresoraremisionsuc.getText());

        if (Integer.valueOf(codigo.getText()) > 0) {
            // Agregar nuevo Cliente
            try {
                us.setCodigo(Integer.valueOf(codigo.getText()));
                grab.actualizarSucursal(us);
                JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
            }
        } else {
            System.out.println("Entro para Agregar");
            try {
                grab.insertarSucursal(us);
                JOptionPane.showMessageDialog(null, "Datos Agregados Correctamente");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
            }
        }
        this.BotonCerrar.doClick();
        GrillaSucursales GrillaPe = new GrillaSucursales();
        Thread HiloGrilla = new Thread(GrillaPe);
        HiloGrilla.start();
        // TODO add your handling code here:
    }//GEN-LAST:event_BotonGrabarActionPerformed

